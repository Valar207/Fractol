#include "libft.h"

void	ft_free_tab_int(int **tab, int size)
{
	int		i;

	i = 0;
	while (i < size)
	{
		ft_memdel((void **)&tab[i]);
		i++;
	}
	ft_memdel((void **)&tab);
}
